#include <iostream>
using namespace std;

int main(){
    int a = 6;
    int b = 4;
    int hasil;

    // penjumlahan
    hasil = a + b;
    cout << a << " + " << b << " = " << hasil << endl;

    // pengurangan
    hasil = a - b;
    cout << a << " - " << b << " = " << hasil << endl;

    // perkalian
    hasil = a * b;
    cout << a << " x " << b << " = " << hasil << endl;

    // pembagian
    float a_bagi = 6;
    float b_bagi = 4;
    float hasil_bagi = a_bagi / b_bagi;
    cout << a_bagi << " / " << b_bagi << " = " << hasil_bagi << endl;

    // Modulus (sisa)
    hasil = a % b ;
    cout << a << " modulus " << b << " = " << hasil << endl;



}